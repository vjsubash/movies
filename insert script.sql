CREATE TABLE MOVIE_CAST(
 ACT_ID INTEGER,
 MOV_ID INTEGER,
 ROLE VARCHAR(10),
 PRIMARY KEY (ACT_ID,MOV_ID),
 FOREIGN KEY (ACT_ID) REFERENCES ACTOR(ACT_ID),
 FOREIGN KEY (MOV_ID) REFERENCES MOVIES(MOV_ID)
);
DESC MOVIE_CAST;

CREATE TABLE RATING(
MOV_ID INTEGER PRIMARY KEY,
REV_STAR VARCHAR(10),
FOREIGN KEY (MOV_ID) REFERENCES MOVIES(MOV_ID)
);
DESC RATING;

